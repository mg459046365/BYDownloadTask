#import "NSString+BYString.h"
#import <CommonCrypto/CommonDigest.h>
#import <CommonCrypto/CommonHMAC.h>

@implementation NSString (BYString)

- (NSString *)by_deleteAllSpaceAndNewline
{
    NSString *content = [self stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceCharacterSet]];
    content =[content stringByReplacingOccurrencesOfString:@"\r" withString:@""];
    content = [content stringByReplacingOccurrencesOfString:@"\n" withString:@""];
    content = [content stringByReplacingOccurrencesOfString:@" " withString:@""];
    
    return (content.length > 0 ? content : nil);
}

- (NSString *)by_md5String
{
    const char *string = self.UTF8String;
    int length = (int)strlen(string);
    unsigned char bytes[CC_MD5_DIGEST_LENGTH];
    CC_MD5(string, length, bytes);
    return [self stringFromBytes:bytes length:CC_MD5_DIGEST_LENGTH];
}

